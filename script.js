/* =============================================
   LANGUAGE DETECTION & SWITCHING
   ============================================= */
let currentLang = 'fr';

/* =============================================
   DARK MODE
   ============================================= */
function initTheme() {
  const saved = localStorage.getItem('portfolio-theme');
  // Default: respect OS preference, fallback light
  const prefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
  const theme = saved || (prefersDark ? 'dark' : 'light');
  applyTheme(theme);
}

function applyTheme(theme) {
  document.documentElement.setAttribute('data-theme', theme);
  localStorage.setItem('portfolio-theme', theme);
  const icon = document.getElementById('themeIcon');
  if (icon) icon.textContent = theme === 'dark' ? '☀️' : '🌙';
}

function toggleTheme() {
  const current = document.documentElement.getAttribute('data-theme') || 'light';
  applyTheme(current === 'dark' ? 'light' : 'dark');
}

document.getElementById('themeToggle').addEventListener('click', toggleTheme);

/* =============================================
   LANGUAGE DETECTION & SWITCHING
   ============================================= */
function detectLanguage() {
  const saved = localStorage.getItem('portfolio-lang');
  if (saved) return saved;

  const browserLang = (navigator.language || navigator.userLanguage || 'fr').slice(0, 2).toLowerCase();
  return browserLang === 'en' ? 'en' : 'fr';
}

function applyTranslations(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = translations[lang][key];
    if (text !== undefined) el.textContent = text;
  });
  document.documentElement.lang = lang;
}

function updateLangToggle(lang) {
  const flag = document.querySelector('.lang-flag');
  const text = document.getElementById('langText');
  if (lang === 'en') {
    flag.textContent = '🇬🇧';
    text.textContent = 'EN';
  } else {
    flag.textContent = '🇫🇷';
    text.textContent = 'FR';
  }
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('portfolio-lang', lang);
  applyTranslations(lang);
  updateLangToggle(lang);
  // Update gallery filter buttons text too
  updateGalleryFiltersText();
}

document.getElementById('langToggle').addEventListener('click', () => {
  setLanguage(currentLang === 'fr' ? 'en' : 'fr');
});

/* =============================================
   PARTICLES BACKGROUND
   ============================================= */
function createParticles() {
  const container = document.getElementById('particles');
  if (!container) return;
  const count = window.innerWidth < 640 ? 15 : 30;
  for (let i = 0; i < count; i++) {
    const p = document.createElement('div');
    p.className = 'particle';
    const size = Math.random() * 6 + 2;
    const x    = Math.random() * 100;
    const dur  = Math.random() * 20 + 12;
    const del  = Math.random() * -15;
    p.style.cssText = `width:${size}px;height:${size}px;left:${x}%;bottom:-10px;animation-duration:${dur}s;animation-delay:${del}s;opacity:${Math.random()*.4+.1}`;
    container.appendChild(p);
  }
}

/* =============================================
   NAVBAR — SCROLL & MOBILE
   ============================================= */
const navbar    = document.getElementById('navbar');
const hamburger = document.getElementById('hamburger');
const navLinks  = document.getElementById('navLinks');

const backToTop = document.getElementById('backToTop');

window.addEventListener('scroll', () => {
  if (window.scrollY > 20) navbar.classList.add('scrolled');
  else navbar.classList.remove('scrolled');
  updateActiveNavLink();
  // Show/hide back-to-top after 400px scroll
  if (backToTop) {
    if (window.scrollY > 400) backToTop.classList.add('visible');
    else backToTop.classList.remove('visible');
  }
}, { passive: true });

if (backToTop) {
  backToTop.addEventListener('click', () => {
    window.scrollTo({ top: 0, behavior: 'smooth' });
  });
}

hamburger.addEventListener('click', () => {
  hamburger.classList.toggle('open');
  navLinks.classList.toggle('open');
});

// Close mobile menu on link click
navLinks.querySelectorAll('.nav-link').forEach(link => {
  link.addEventListener('click', () => {
    hamburger.classList.remove('open');
    navLinks.classList.remove('open');
  });
});

function updateActiveNavLink() {
  const sections = document.querySelectorAll('section[id]');
  let current = '';
  sections.forEach(s => {
    if (window.scrollY >= s.offsetTop - 120) current = s.id;
  });
  document.querySelectorAll('.nav-link').forEach(link => {
    link.classList.toggle('active', link.getAttribute('href') === `#${current}`);
  });
}

/* =============================================
   SMOOTH SCROLL
   ============================================= */
document.querySelectorAll('a[href^="#"]').forEach(a => {
  a.addEventListener('click', e => {
    const target = document.querySelector(a.getAttribute('href'));
    if (target) {
      e.preventDefault();
      const offset = target.getBoundingClientRect().top + window.scrollY - 80;
      window.scrollTo({ top: offset, behavior: 'smooth' });
    }
  });
});

/* =============================================
   SCROLL REVEAL (AOS-like)
   ============================================= */
function initScrollReveal() {
  const items = document.querySelectorAll('[data-aos]');
  const obs = new IntersectionObserver(entries => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        const delay = parseInt(e.target.dataset.aosDelay || 0);
        setTimeout(() => e.target.classList.add('aos-animate'), delay);
        obs.unobserve(e.target);
      }
    });
  }, { threshold: 0.12, rootMargin: '0px 0px -60px 0px' });
  items.forEach(el => obs.observe(el));
}

/* =============================================
   COUNTER ANIMATION
   ============================================= */
function animateCounters() {
  const counters = document.querySelectorAll('.stat-num[data-count]');
  const obs = new IntersectionObserver(entries => {
    entries.forEach(e => {
      if (!e.isIntersecting) return;
      const el     = e.target;
      const target = parseInt(el.dataset.count);
      const dur    = 1800;
      const step   = 16;
      const inc    = target / (dur / step);
      let cur      = 0;
      const timer  = setInterval(() => {
        cur += inc;
        if (cur >= target) { el.textContent = target; clearInterval(timer); }
        else el.textContent = Math.floor(cur);
      }, step);
      obs.unobserve(el);
    });
  }, { threshold: 0.5 });
  counters.forEach(el => obs.observe(el));
}

/* =============================================
   GALLERY FILTER
   ============================================= */
function updateGalleryFiltersText() {
  const filterBtns = document.querySelectorAll('.gal-filter');
  filterBtns.forEach(btn => {
    const key = btn.dataset.i18n;
    if (key && translations[currentLang][key]) {
      btn.textContent = translations[currentLang][key];
    }
  });
}

function initGallery() {
  const filters = document.querySelectorAll('.gal-filter');
  const items   = document.querySelectorAll('.gal-item');

  filters.forEach(btn => {
    btn.addEventListener('click', () => {
      const filter = btn.dataset.filter;

      // Update active state
      filters.forEach(b => b.classList.remove('active'));
      btn.classList.add('active');

      // Filter items
      items.forEach(item => {
        const cat = item.dataset.cat;
        if (filter === 'all' || cat === filter) {
          item.style.display = '';
          item.classList.remove('hidden');
        } else {
          item.style.display = 'none';
          item.classList.add('hidden');
        }
      });
    });
  });
}

/* =============================================
   LIGHTBOX
   ============================================= */
function initLightbox() {
  const lightbox  = document.getElementById('lightbox');
  const lbImg     = document.getElementById('lbImg');
  const lbCaption = document.getElementById('lbCaption');
  const lbClose   = document.getElementById('lbClose');

  document.querySelectorAll('.gal-item').forEach(item => {
    item.addEventListener('click', () => {
      const img     = item.querySelector('img');
      const caption = item.querySelector('.gal-overlay h3')?.textContent || '';
      lbImg.src     = img.src;
      lbImg.alt     = img.alt;
      lbCaption.textContent = caption;
      lightbox.classList.add('open');
      document.body.style.overflow = 'hidden';
    });
  });

  lbClose.addEventListener('click', closeLightbox);
  lightbox.addEventListener('click', e => { if (e.target === lightbox) closeLightbox(); });
  document.addEventListener('keydown', e => { if (e.key === 'Escape') closeLightbox(); });

  function closeLightbox() {
    lightbox.classList.remove('open');
    document.body.style.overflow = '';
    lbImg.src = '';
  }
}

/* =============================================
   CONTACT FORM
   ============================================= */
function initForm() {
  const form    = document.getElementById('contactForm');
  const btn     = document.getElementById('submitBtn');
  const success = document.getElementById('formSuccess');
  if (!form) return;

  form.addEventListener('submit', e => {
    e.preventDefault();

    const name  = document.getElementById('fname')?.value.trim();
    const email = document.getElementById('femail')?.value.trim();
    const msg   = document.getElementById('fmessage')?.value.trim();

    if (!name || !email || !msg) return;
    if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
      document.getElementById('femail').style.borderColor = '#e74c3c';
      return;
    }

    // Simulate send
    btn.disabled = true;
    btn.querySelector('span').textContent = currentLang === 'fr' ? 'Envoi...' : 'Sending...';

    setTimeout(() => {
      form.reset();
      btn.disabled = false;
      btn.querySelector('span').textContent = translations[currentLang]['form.send'];
      success.classList.add('visible');
      setTimeout(() => success.classList.remove('visible'), 4000);
    }, 1200);
  });
}

/* =============================================
   INIT ON LOAD
   ============================================= */
document.addEventListener('DOMContentLoaded', () => {
  // 0. Theme (dark/light) — must be first
  initTheme();

  // 1. Detect and apply language
  currentLang = detectLanguage();
  applyTranslations(currentLang);
  updateLangToggle(currentLang);

  // 2. Particles
  createParticles();

  // 3. Scroll effects
  initScrollReveal();

  // 4. Counter
  animateCounters();

  // 5. Gallery
  initGallery();

  // 6. Lightbox
  initLightbox();

  // 7. Form
  initForm();

  console.log(`Portfolio François Djoman Abeto — Loaded (${currentLang.toUpperCase()}) ✓`);
});
